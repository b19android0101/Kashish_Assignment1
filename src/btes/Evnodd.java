package btes;
class Evnodd
{
public static void main(String args[])
{
System.out.println("Odd numbers are ");
for(int i=1;i<=100;i+=2)
{
System.out.print(" "+i);
}
System.out.println("");
System.out.println("Even numbers are ");
for(int j=2;j<=100;j+=2)
{
System.out.print(" "+j);
}
}
}
